package org.merka.stubgen.classloader;

public class RawClass
{
	private byte[] rawClassBytes;
	public byte[] getRawClassBytes()
	{
		return rawClassBytes;
	}
	
	protected void setRawClassBytes(byte[] rawClassBytes)
	{
		this.rawClassBytes = rawClassBytes;
	}

	private String name;
	public String getName()
	{
		return name;
	}
	protected void setName(String name)
	{
		this.name = name;
	}
	
	public RawClass(byte[] rawClassBytes, String name)
	{
		setName(name);
		setRawClassBytes(rawClassBytes);
	}
}
