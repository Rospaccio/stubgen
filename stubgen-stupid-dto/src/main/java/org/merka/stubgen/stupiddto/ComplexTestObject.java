package org.merka.stubgen.stupiddto;

import java.math.BigDecimal;
import java.util.Arrays;
import java.util.List;

public class ComplexTestObject
{
	public enum TestType
	{
		OK, KO, UNKNOWN
	}
	
	protected List<TestObject> objectList;
	protected TestObject[] objectArray;
	protected TestType type;
	protected TestObject singleObject;
	protected BigDecimal[] bigDecimalArray;
	
	public BigDecimal[] getBigDecimalArray()
	{
		return bigDecimalArray;
	}


	public void setBigDecimalArray(BigDecimal[] bigDecimalArray)
	{
		this.bigDecimalArray = bigDecimalArray;
	}

	public List<TestObject> getObjectList()
	{
		return objectList;
	}

	public void setObjectList(List<TestObject> objectList)
	{
		this.objectList = objectList;
	}


	public TestObject[] getObjectArray()
	{
		return objectArray;
	}

	public void setObjectArray(TestObject[] objectArray)
	{
		this.objectArray = objectArray;
	}


	public TestType getType()
	{
		return type;
	}


	public void setType(TestType type)
	{
		this.type = type;
	}


	public TestObject getSingleObject()
	{
		return singleObject;
	}


	public void setSingleObject(TestObject singleObject)
	{
		this.singleObject = singleObject;
	}


	public ComplexTestObject()
	{
		
	}

	@Override
	public String toString() {
		return "ComplexTestObject [objectList=" + objectList + ", objectArray="
				+ Arrays.toString(objectArray) + ", type=" + type
				+ ", singleObject=" + singleObject + ", bigDecimalArray="
				+ Arrays.toString(bigDecimalArray) + "]";
	}
	
	
	
}
